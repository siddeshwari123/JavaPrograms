// datatypesdemo

public class Datatypes{
	
	public static void main(String[] args)
	{

	byte b = 10;
	short s= 3000;
	int i =50000;
	long l=100000000l;

	float f= 3.4f;
	double d =95.565656d;

	char c ='S';

	boolean flag=true;

	System.out.println("boolean is:"+flag);
	System.out.println("byte variable is :"+b);
	System.out.println("short variable is :"+s);
	System.out.println("int variable is :"+i);
	System.out.println("long variable is :"+l);
	System.out.println("flag variable is :"+f);
	System.out.println(" double variable is :"+d);
	System.out.println("char variable is:"+c);
	}
}