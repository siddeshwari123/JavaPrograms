//program to demonstrate Arithmatic Operator
public class ArithmaticOperator{
	
	public static void main(String[] args){

	 int a=20;
	 int b=50;

	 int sum=a+b;
	 int difference=a-b;
	 int product=a*b;
	 int quotient=a/b;
	 int remainder=a%b;

	 System.out.println("addition is :"+ sum);
	 System.out.println("Subtraction is:"+ difference);
	 System.out.println("Multiplication is:" +product);
	 System.out.println("division is:"+quotient);
	 System.out.println("modules is:"+ remainder);
	}
}